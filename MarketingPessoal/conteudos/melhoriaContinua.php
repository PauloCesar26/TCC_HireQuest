                    <h3>1. Feedback Formal e Informal</h3>
                    <p class="text">O feedback pode ser formal, como avaliações de desempenho e reuniões de feedback programadas, ou informal, como conversas cotidianas com colegas e supervisores. Ambos os tipos são importantes e podem fornecer insights valiosos para o desenvolvimento profissional.</p>
